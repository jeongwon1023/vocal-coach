def lesson_opening(strengths: list[str], overall: float) -> str:
    if not strengths:
        if overall >= 70:
            return "오늘도 수고 많았어요. 전반적으로 안정적인 편이에요."
        return "오늘도 용기 내서 불러줘서 고마워요. 같이 하나씩 잡아봐요."
    lead = strengths[0]
    if len(strengths) >= 2:
        return (
            f"먼저 칭찬부터 할게요. **{lead}** "
            f"그리고 {strengths[1]} — 이건 꽤 잘하고 있는 부분이에요."
        )
    return f"먼저 칭찬부터 할게요. **{lead}** — 이건 꽤 잘하고 있는 부분이에요."


